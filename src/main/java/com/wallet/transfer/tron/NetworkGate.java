package com.wallet.transfer.tron;

/**
 * Пропускной контроль по сети перед созданием клиента Tron.
 * <p>
 * Точка расширения между открытым ядром и закрытой частью приложения:
 * открытое ядро объявляет контракт и содержит нейтральную реализацию,
 * которая пропускает любую сеть; закрытая часть может подменить бин
 * реализацией, требующей лицензию для отдельных сетей (например, mainnet).
 * Так открытый код остаётся самодостаточным и не содержит логики
 * лицензирования, а граница между слоями — чистой.
 */
public interface NetworkGate {

    /**
     * Проверить, разрешена ли работа в данной сети.
     *
     * @param network идентификатор сети (nile, shasta, mainnet)
     * @throws NetworkNotAllowedException если работа в сети запрещена
     */
    void ensureAllowed(String network);
}
