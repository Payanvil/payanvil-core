package com.wallet.transfer.tron;

/**
 * Итог обработки одного перевода в пачке.
 */
public enum TransferOutcome {
    /** Отправлен и подтверждён сетью (CONFIRMED). */
    CONFIRMED,
    /** Отправлен, но статус ещё не подтверждён (остался SENT). */
    SENT_UNCONFIRMED,
    /** Пропущен из-за проблемы в самом переводе (битый адрес, revert). */
    SKIPPED,
    /** Обработка пачки остановлена на этом переводе (нехватка средств/сеть). */
    STOPPED,
    /** Транзакция исполнилась с ошибкой в сети (FAILED). */
    FAILED,
    /** Комиссия в пересчёте на USDT ≥ суммы перевода — перевод не отправлен. */
    FEE_EXCEEDS_AMOUNT
}
