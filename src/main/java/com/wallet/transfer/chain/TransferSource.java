package com.wallet.transfer.chain;

/**
 * Источник пакета переводов — откуда пришли платёжные данные.
 * Пишется в каждый перевод для аудита истории отправок.
 * <p>
 * CLI считается источником {@link #FILE}: значение описывает происхождение
 * данных (входной файл), а не способ запуска приложения.
 */
public enum TransferSource {

    /** Загружено из входного файла (файловый экран GUI или CLI). */
    FILE,

    /** Получено через Telegram-бота. */
    TELEGRAM
}
