package com.wallet.transfer.domain;

/**
 * Жизненный цикл перевода.
 * <p>
 * Статус отражает, на какой стадии обработки находится перевод:
 * <pre>
 * PARSED          — распарсен из входа, сохранён, ждёт обработки
 * BALANCE_CHECKED — проверены балансы TRX и USDT отправителя
 * PREPARED        — транзакция построена и подписана, готова к отправке
 * SENT            — отправлена в сеть, ждёт подтверждения
 * CONFIRMED       — подтверждена сетью (финальный успех)
 * FAILED          — ошибка на любой из стадий (финальный провал)
 * </pre>
 * Доменное понятие: живёт в открытом ядре, персистентный слой лишь
 * маппит его в БД.
 */
public enum TransferStatus {
    PARSED,
    BALANCE_CHECKED,
    PREPARED,
    SENT,
    CONFIRMED,
    FAILED
}
